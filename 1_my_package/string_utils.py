""" Модуль для роботи з рядками """


def upper(string):
    """
    Функція, яка повертає перетворений текст у верхній регістр.

    :param string: Рядок для перетворення.
    :return: Рядок у верхньому регістрі
    """
    return string.upper()


def strip(string):
    """
    Функція, яка повертає результат з видаленими пробілами та початку та в кінці рядка

    :param string: Рядок для обробки.
    :return: Рядок без пробілів на початку та в кінці.
    """
    return string.strip()
